package projeto_java_bd;

public abstract class Modificar  {
    protected int cor;
    protected int modificar;

    //GETTER E SETTER
    public int getCor() {
        return cor;
    }

    public void setCor(int cor) {
        this.cor = cor;
    }

    public int getModificar() {
        return modificar;
    }

    public void setModificar(int modificar) {
        this.modificar = modificar;
    }
    
}
